function getTemplate(docType) {
  return TEMPLATES[docType] || null;
}

// Текстовый чек-лист для системного промпта Интервьюера.
function buildChecklist(docType) {
  const t = TEMPLATES[docType];
  if (!t) return '';
  const req = t.requiredFields.map((f) => `  • [ОБЯЗАТЕЛЬНО] ${f.title}: ${f.hint}`).join('\n');
  const opt = (t.optionalFields || []).map((f) => `  • [желательно] ${f.title}: ${f.hint}`).join('\n');
  return `Тип документа: ${t.label}.\nНеобходимые сведения для составления:\n${req}${opt ? '\n' + opt : ''}`;
}

module.exports = { TEMPLATES, getTemplate, buildChecklist };
