// ── Построение Map<alias, canonical> один раз при загрузке модуля ───────
const ALIAS_MAP = new Map();
for (const { canonical, aliases } of CANONICAL_NPAS) {
    // канонический тоже считаем алиасом самого себя
    ALIAS_MAP.set(canonical.toLowerCase(), canonical);
    for (const a of aliases) {
        ALIAS_MAP.set(a.toLowerCase(), canonical);
    }
}

// ── Нормализация ключа ──────────────────────────────────────────────────
// • lowercase
// • схлопывание whitespace
// • удаление трейлинг-точек (на случай "УК КР.")
// • trim
function _key(s) {
    return String(s || '')
        .toLowerCase()
        .replace(/\s+/g, ' ')
        .replace(/\.+\s*$/, '')
        .trim();
}

function normalizeNpaName(input) {
    if (input === null || input === undefined) return '';
    const raw = String(input).trim();
    if (!raw) return '';
    const key = _key(raw);
    const canonical = ALIAS_MAP.get(key);
    if (canonical) return canonical;
    // Дополнительно: если ключ начинается с известного алиаса + хвост ("УК КР глава 1")
    // — попробуем вытянуть первый кусок до пробела/цифры.
    const headMatch = key.match(/^[a-zа-яё.\s]+?(?=\s+\d|\s+глава|\s+стать|\s+часть|$)/u);
    if (headMatch) {
        const head = _key(headMatch[0]);
        if (head && ALIAS_MAP.has(head)) return ALIAS_MAP.get(head);
    }
    return raw; // unknown NPA — возвращаем как есть
}

